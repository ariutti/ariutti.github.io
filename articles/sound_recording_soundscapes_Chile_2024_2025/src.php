<?php genheader("Sound Recording Series: Soundscapes, Chile 2024/2025", "January 18, 2025");?>

<p>For the Christmas holidays, my wife and I spent a few weeks in her home country, Chile. We stayed in Santiago for a few days before heading south to the Puerto Varas area and the island of Chiloé.</p>

<p>As we've been doing during our vacations lately, this time we also brought along our gear for some field recording: a small Zoom H1n recorder with a compact windscreen for windy days.</p>

<p>We conducted several recording sessions, some while standing still at specific points of interest, and others while walking silently through the vegetation, admiring the landscape, and listening to the surrounding sounds (soundwalk).</p>

<p>As I usually do, I’ve uploaded the audio files to my favorite sound-sharing platform, <b>freesound.org</b><?php footnote("freesound.org platform", "https://freesound.org");?>, in the pack <b>Chile_24_25</b><?php footnote("freesound 'Chile 24-25 sound pack'","https://freesound.org/people/nicola_ariutti/packs/42642/");?>, complete with geolocation.</p>

<p>I choose to minimize any post-processing of the audio files: I didn’t use equalizers or adjust dynamics. I only made a few cuts here and there to remove the most evident anthropogenic noises, but I deliberately left everything else untouched. So you might hear the sounds of our footsteps - whether it’s on wooden walkways covered in dry leaves or on gravel paths. You might hear an occasional breath or even the occasional bump from my clumsy handling of the recorder. I decided to leave the recordings as intact as possible, staying true to the acoustic experience we lived through.</p>

