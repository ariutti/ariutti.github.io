<h2>Pasarela</h2>

<p>Recorded on January 6, 2025 around 21:30</p>

<p>Field-recording of a soundwalk along a wooden boardwalk on a very flat beach. Various species of water and marsh birds. Light wind. A few dogs barking in the distance. Sounds of cars and traffic in the distance. Some human voices in the background. In the distance, the sound of people tinkering with metal structures can be heard.</p>

<p>Walking we gradually approach the island. The sounds of the undertow of the waves and the calls of the hawks (<i>tiuque</i>) that have their nests in the trees on the island are evident.</p>

